function byId(id) {
  return document.getElementById(id);
}

function applyLang(lang) {
  const dict = I18N[lang] || I18N.zh;
  document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";
  localStorage.setItem("ta_lang", lang);

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (!dict[key]) return;
    el.textContent = dict[key];
  });

  const toggle = byId("lang-toggle");
  if (toggle) toggle.textContent = lang === "zh" ? "EN" : "中文";
}

function setFooterYear() {
  const y = byId("year");
  if (y) y.textContent = String(new Date().getFullYear());
}

function attachPretext(element, options = {}) {
  if (!element || element.dataset.pretextBound === "true") return;
  element.dataset.pretextBound = "true";
  element.classList.add("pretext-field");

  const moveXFactor = options.moveXFactor ?? 0.022;
  const moveYFactor = options.moveYFactor ?? 0.018;
  const skewFactor = options.skewFactor ?? 0.005;
  const shadowXFactor = options.shadowXFactor ?? 0.03;
  const shadowYFactor = options.shadowYFactor ?? 0.014;
  const spacingBase = options.spacingBase ?? 0;
  const spacingRange = options.spacingRange ?? 0.03;

  element.addEventListener("pointermove", (event) => {
    const rect = element.getBoundingClientRect();
    const offsetX = event.clientX - (rect.left + rect.width / 2);
    const offsetY = event.clientY - (rect.top + rect.height / 2);
    const moveX = offsetX * moveXFactor;
    const moveY = offsetY * moveYFactor;
    const skew = offsetX * skewFactor;
    const spacing = spacingBase + Math.min(Math.abs(offsetX) / 800, spacingRange);

    element.style.transform = `translate3d(${moveX.toFixed(2)}px, ${moveY.toFixed(2)}px, 0) skewX(${skew.toFixed(2)}deg)`;
    element.style.textShadow = `${(offsetX * shadowXFactor).toFixed(2)}px ${(offsetY * shadowYFactor).toFixed(2)}px 0 rgba(255,255,255,0.68), ${(-offsetX * shadowXFactor * 0.55).toFixed(2)}px ${(-offsetY * shadowYFactor * 0.55).toFixed(2)}px 0 rgba(205,163,59,0.28)`;
    element.style.letterSpacing = `${spacing.toFixed(3)}em`;
    element.classList.add("is-active");
  });

  element.addEventListener("pointerleave", () => {
    element.style.transform = "";
    element.style.textShadow = "";
    element.style.letterSpacing = "";
    element.classList.remove("is-active");
  });
}

function attachMagneticPull(element) {
  if (!element || element.dataset.magneticBound === "true") return;
  element.dataset.magneticBound = "true";
  element.classList.add("pretext-magnetic");

  element.addEventListener("pointermove", (event) => {
    const rect = element.getBoundingClientRect();
    const offsetX = event.clientX - (rect.left + rect.width / 2);
    const offsetY = event.clientY - (rect.top + rect.height / 2);
    const moveX = Math.max(Math.min(offsetX * 0.12, 12), -12);
    const moveY = Math.max(Math.min(offsetY * 0.12, 10), -10);
    element.style.setProperty("--mx", `${moveX.toFixed(2)}px`);
    element.style.setProperty("--my", `${moveY.toFixed(2)}px`);
    element.classList.add("is-pulled");
  });

  element.addEventListener("pointerleave", () => {
    element.style.setProperty("--mx", "0px");
    element.style.setProperty("--my", "0px");
    element.classList.remove("is-pulled");
  });
}

function initMotion() {
  const allowMotion = window.matchMedia("(pointer: fine)").matches &&
    !window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  if (!allowMotion) return;

  document.querySelectorAll("[data-pretext]").forEach((element) => {
    const mode = element.getAttribute("data-pretext");
    attachPretext(element, mode === "intense" ? {
      moveXFactor: 0.03,
      moveYFactor: 0.024,
      skewFactor: 0.007,
      shadowXFactor: 0.05,
      shadowYFactor: 0.02,
      spacingBase: -0.03,
      spacingRange: 0.09
    } : {});
  });

  document.querySelectorAll("[data-magnetic]").forEach(attachMagneticPull);

  if (document.body.dataset.page === "home") {
    const heroMedia = document.querySelector("[data-parallax='hero-media'] img");
    const heroFocus = document.querySelector("[data-parallax='hero-host']");
    const hero = document.querySelector(".story-hero");
    if (hero && heroMedia) {
      hero.addEventListener("pointermove", (event) => {
        const rect = hero.getBoundingClientRect();
        const offsetX = event.clientX - (rect.left + rect.width / 2);
        const offsetY = event.clientY - (rect.top + rect.height / 2);
        heroMedia.style.transform = `scale(1.02) translate3d(${(offsetX * 0.0035).toFixed(2)}px, ${(offsetY * 0.0045).toFixed(2)}px, 0)`;
        if (heroFocus) {
          heroFocus.style.transform = `translate3d(${(offsetX * 0.012).toFixed(2)}px, ${(offsetY * 0.01).toFixed(2)}px, 0)`;
        }
      });
      hero.addEventListener("pointerleave", () => {
        heroMedia.style.transform = "scale(1.01)";
        if (heroFocus) {
          heroFocus.style.transform = "translate3d(0, 0, 0)";
        }
      });
    }
  }
}

function pickAssets(assets) {
  const list = Array.isArray(assets) ? assets : [];
  return {
    win: list.find((asset) => /windows\.zip/i.test(asset.name || "")),
    macZip: list.find((asset) => /mac\.zip/i.test(asset.name || "")),
    macDmg: list.find((asset) => /mac\.dmg/i.test(asset.name || ""))
  };
}

function setAssetLink(anchorId, nameId, asset, fallbackText) {
  const anchor = byId(anchorId);
  const nameNode = byId(nameId);
  if (!anchor || !nameNode) return;

  if (!asset || !asset.url) {
    nameNode.textContent = fallbackText;
    anchor.classList.add("disabled");
    anchor.setAttribute("aria-disabled", "true");
    anchor.href = "#";
    return;
  }

  nameNode.textContent = asset.name;
  anchor.classList.remove("disabled");
  anchor.removeAttribute("aria-disabled");
  anchor.href = asset.url;
}

async function fetchManifest() {
  const localRes = await fetch("./api/release-manifest.json", { cache: "no-store" });
  if (!localRes.ok) throw new Error(`manifest status ${localRes.status}`);
  return await localRes.json();
}

async function fetchGithubFallback() {
  const repo = "tongxigogo-crypto/foreign-trade-assistant";
  const res = await fetch(`https://api.github.com/repos/${repo}/releases/latest`, { cache: "no-store" });
  if (!res.ok) throw new Error(`github status ${res.status}`);
  const data = await res.json();
  return {
    version: data.tag_name || "unknown",
    published_at: data.published_at || null,
    assets: (data.assets || []).map((asset) => ({ name: asset.name, url: asset.browser_download_url }))
  };
}

async function loadReleaseData() {
  try {
    return await fetchManifest();
  } catch {
    try {
      return await fetchGithubFallback();
    } catch {
      return { version: "unavailable", assets: [] };
    }
  }
}

async function initReleaseData() {
  const manifest = await loadReleaseData();
  const versionNodes = [byId("version-tag"), byId("home-version-tag")].filter(Boolean);
  versionNodes.forEach((node) => {
    node.textContent = manifest.version || "unknown";
  });

  const assets = pickAssets(manifest.assets || []);
  setAssetLink("win-download", "win-asset-name", assets.win, "not available");
  setAssetLink("mac-download-dmg", "mac-dmg-asset-name", assets.macDmg, "not available");
  setAssetLink("mac-download-zip", "mac-zip-asset-name", assets.macZip, "not available");
}

function init() {
  const lang = localStorage.getItem("ta_lang") || "zh";
  applyLang(lang);
  setFooterYear();
  initMotion();
  initReleaseData();

  const toggle = byId("lang-toggle");
  if (toggle) {
    toggle.addEventListener("click", () => {
      const next = (localStorage.getItem("ta_lang") || "zh") === "zh" ? "en" : "zh";
      applyLang(next);
    });
  }
}

init();

